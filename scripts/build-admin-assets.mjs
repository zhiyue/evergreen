import { readdir, readFile, writeFile } from "node:fs/promises";
import { join } from "node:path";

const assetsDir = "dist/admin/assets";
const files = await readdir(assetsDir);
const css = await readAssets(files.filter((file) => file.endsWith(".css")));
const js = await readAssets(files.filter((file) => file.endsWith(".js")));

await writeFile(
  "src/admin-assets.ts",
  `// Generated by scripts/build-admin-assets.mjs. Do not edit by hand.\nexport const ADMIN_CSS = ${JSON.stringify(css)};\nexport const ADMIN_JS = ${JSON.stringify(js)};\n`,
);

async function readAssets(names) {
  const ordered = names.sort();
  return (await Promise.all(ordered.map((name) => readFile(join(assetsDir, name), "utf8")))).join("\n");
}
